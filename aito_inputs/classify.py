def classify_task_type(text: str) -> str:
    """按规则判定 task_type；无命中默认「直接问答」。"""
    t = text or ""
    low = t.lower()
    for task_type, keywords in _TASK_TYPE_RULES:
        for kw in keywords:
            if kw in t or kw in low:
                return task_type
    return "直接问答"


def risk_tags(text: str) -> list[str]:
    """返回命中的主题标签列表（去重、保持规则定义顺序）。"""
    t = text or ""
    low = t.lower()
    tags: list[str] = []
    for tag, keywords in _RISK_TAG_RULES.items():
        if any(kw in t or kw in low for kw in keywords):
            tags.append(tag)
    return tags
